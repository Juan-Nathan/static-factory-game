package game.capabilities;

/**
 * Enumerates different abilities that can be possessed by Actors or other entities within the game.
 * Example #1: If the Player is capable of jumping over walls, you can attach Ability.WALL_JUMP to the Player class.
 */

public enum Ability {

    /**
     * Ability to enter Floors.
     */
    ENTER_FLOOR,


    /**
     * Ability of the actor to buy scraps
     */

    BUY_SCRAPS





}
